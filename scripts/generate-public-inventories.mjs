import { createHash } from "node:crypto";
import { readFile, readdir, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(SCRIPT_DIR, "..");
const AUDIO_CATALOG_PATH = path.join(ROOT, "public", "assets", "audio", "catalog.json");
const OFFICIAL_ASSET_LICENSE = "LicenseRef-Meowthology-Official-Builtin";
const CUTSCENE_LICENSE = "LicenseRef-Cat-Odyssey-ElevenLabs-NC-1.0";

const sourceCategoryByLegacyType = {
  "suno-bgm": "suno-paid-dedicated",
  "user-owned-remaster": "suno-paid-user-owned-remaster",
  "user-owned-cross-project-suno-sfx": "suno-paid-user-owned",
  "deterministic-procedural-master": "project-owned-procedural",
};

const inputCatalog = JSON.parse(await readFile(AUDIO_CATALOG_PATH, "utf8"));
const audioEntries = [...inputCatalog.bgm, ...inputCatalog.sfx];
const audioOriginBySource = new Map();

for (const entry of audioEntries) {
  const sourceCategory = entry.origin?.category
    ?? sourceCategoryByLegacyType[entry.source?.type];
  if (!sourceCategory) throw new Error(`Unknown audio origin category for ${entry.id}`);
  audioOriginBySource.set(entry.src, sourceCategory);
}

const sanitizedCatalog = {
  schemaVersion: 1,
  description: "Runtime audio integrity and public provenance categories.",
  bgm: await Promise.all(inputCatalog.bgm.map((entry) => sanitizeAudioEntry(entry))),
  sfx: await Promise.all(inputCatalog.sfx.map((entry) => sanitizeAudioEntry(entry))),
  provenanceCategories: {
    "suno-paid-dedicated": "Generated for Cat Odyssey while the rights holder had a paid Suno plan.",
    "suno-paid-user-owned-remaster": "User-owned remaster music generated while the rights holder had a paid Suno plan.",
    "suno-paid-user-owned": "User-owned Suno audio generated while the rights holder had a paid plan and adapted for this game.",
    "project-owned-procedural": "Deterministically synthesized by the project and not generated by Suno.",
  },
};

await writeJson(AUDIO_CATALOG_PATH, sanitizedCatalog);

const binaryRoots = [
  path.join(ROOT, "public", "assets", "art"),
  path.join(ROOT, "public", "assets", "audio", "bgm"),
  path.join(ROOT, "public", "assets", "audio", "sfx"),
  path.join(ROOT, "public", "assets", "video"),
  path.join(ROOT, "cartridge", "assets"),
];

const inventory = [];
for (const root of binaryRoots) {
  for (const filePath of await walk(root)) {
    inventory.push(await inventoryEntry(filePath));
  }
}
inventory.sort((left, right) => left.path.localeCompare(right.path, "en"));

await writeJson(path.join(ROOT, "ASSET_INVENTORY.json"), {
  schemaVersion: 1,
  algorithm: "SHA-256",
  generatedFrom: "version-controlled runtime assets",
  assetCount: inventory.length,
  totalBytes: inventory.reduce((sum, entry) => sum + entry.bytes, 0),
  assets: inventory,
});

await writeJson(path.join(ROOT, "MEDIA_MANIFEST.json"), {
  schemaVersion: 1,
  delivery: "git-lfs",
  checkoutRequirement: "Run git lfs install and git lfs pull before building or packaging.",
  fileCount: inventory.length,
  totalBytes: inventory.reduce((sum, entry) => sum + entry.bytes, 0),
  files: inventory.map(({ path: filePath, bytes, sha256 }) => ({ path: filePath, bytes, sha256 })),
});

const packageLock = JSON.parse(await readFile(path.join(ROOT, "package-lock.json"), "utf8"));
const packages = Object.entries(packageLock.packages ?? {})
  .filter(([packagePath]) => packagePath)
  .map(([packagePath, metadata]) => ({
    name: packageNameFromLockPath(packagePath, metadata),
    version: metadata.version ?? "unknown",
    license: metadata.license ?? "NOASSERTION",
    developmentOnly: metadata.dev === true,
    optional: metadata.optional === true,
  }))
  .sort((left, right) => left.name.localeCompare(right.name, "en") || left.version.localeCompare(right.version, "en"));

await writeJson(path.join(ROOT, "THIRD_PARTY_PACKAGES.json"), {
  schemaVersion: 1,
  source: "package-lock.json",
  packageCount: packages.length,
  missingLicenseCount: packages.filter((entry) => entry.license === "NOASSERTION").length,
  packages,
});

console.log(JSON.stringify({
  status: "ok",
  audioEntries: sanitizedCatalog.bgm.length + sanitizedCatalog.sfx.length,
  assets: inventory.length,
  packages: packages.length,
}, null, 2));

async function sanitizeAudioEntry(entry) {
  const filePath = path.join(ROOT, "public", ...entry.src.split("/"));
  const content = await readFile(filePath);
  const originCategory = audioOriginBySource.get(entry.src);
  return {
    id: entry.id,
    src: entry.src,
    durationSeconds: entry.durationSeconds ?? entry.duration,
    bytes: content.length,
    sha256: sha256(content),
    role: entry.role,
    ...(entry.semanticFamily ? { semanticFamily: entry.semanticFamily } : {}),
    origin: {
      category: originCategory,
      rightsStatus: originCategory === "project-owned-procedural"
        ? "project-owned"
        : "rights-holder-confirmed-paid-plan-commercial-rights",
    },
    licenseId: OFFICIAL_ASSET_LICENSE,
  };
}

async function inventoryEntry(filePath) {
  const content = await readFile(filePath);
  const relativePath = toPosix(path.relative(ROOT, filePath));
  const extension = path.extname(filePath).toLowerCase();
  const common = {
    path: relativePath,
    mediaType: mediaType(extension),
    bytes: content.length,
    sha256: sha256(content),
    delivery: "git-lfs",
  };

  if (relativePath.startsWith("public/assets/audio/")) {
    const runtimeSource = relativePath.replace(/^public\//, "");
    const originCategory = audioOriginBySource.get(runtimeSource);
    if (!originCategory) throw new Error(`Missing audio provenance for ${relativePath}`);
    return {
      ...common,
      originCategory,
      rightsStatus: originCategory === "project-owned-procedural"
        ? "project-owned"
        : "rights-holder-confirmed-paid-plan-commercial-rights",
      licenseId: OFFICIAL_ASSET_LICENSE,
    };
  }

  if (relativePath.startsWith("public/assets/video/cutscenes/")) {
    const episode = Number(path.basename(filePath).match(/^ep(\d+)\.mp4$/i)?.[1]);
    if (!Number.isInteger(episode)) throw new Error(`Unexpected cutscene filename: ${relativePath}`);
    return {
      ...common,
      originCategory: episode <= 11
        ? "user-supplied-remaster-final"
        : "project-produced-final",
      narrationProvider: "ElevenLabs",
      narrationPlanStatus: episode <= 11 ? "unknown" : "free-plan-confirmed",
      generationMode: "standard-text-to-speech",
      generationModeEvidence: "release-audit-confirmed-elevenlabs-text-to-speech",
      voiceName: episode <= 11
        ? "historical ElevenLabs voice name not preserved"
        : "Spuds Oxley - Wise and Approachable (ElevenLabs Voice Library)",
      voiceUseDisclosure: "provider-available voice selected for Text to Speech; user reports no self-created voice clone",
      voiceAuthorizationEvidence: "user-confirmed-no-self-created-voice-clone",
      inputRightsEvidence: "user-controlled AI-assisted narration script",
      rightsStatus: episode <= 11
        ? "release-approved-noncommercial; plan-unknown-treated-as-free-plan"
        : "release-approved-noncommercial; free-plan-confirmed",
      licenseId: CUTSCENE_LICENSE,
      attribution: "elevenlabs.io",
      attributionSurface: "embedded-mp4-title; in-game-title; replay-list; package-notice; release-documentation",
      derivativeScope: "video-and-derivatives-retaining-this-narration-are-non-commercial-only",
      commercialReplacement: "new-paid-plan-period-narration-and-rebuilt-video-required",
      providerTerms: "https://elevenlabs.io/terms-of-use",
    };
  }

  return {
    ...common,
    originCategory: "openai-codex-generated-image",
    rightsStatus: "rights-holder-confirmed-commercial-and-redistribution-rights",
    licenseId: OFFICIAL_ASSET_LICENSE,
  };
}

function mediaType(extension) {
  if (extension === ".png") return "image/png";
  if (extension === ".webp") return "image/webp";
  if (extension === ".mp3") return "audio/mpeg";
  if (extension === ".mp4") return "video/mp4";
  return "application/octet-stream";
}

function packageNameFromLockPath(packagePath, metadata) {
  if (metadata.name) return metadata.name;
  const marker = "node_modules/";
  const index = packagePath.lastIndexOf(marker);
  return index >= 0 ? packagePath.slice(index + marker.length) : packagePath;
}

async function writeJson(targetPath, value) {
  await writeFile(targetPath, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

function sha256(content) {
  return createHash("sha256").update(content).digest("hex");
}

function toPosix(value) {
  return value.replace(/\\/g, "/").normalize("NFC");
}

async function walk(root) {
  const output = [];
  async function visit(directory) {
    let entries;
    try {
      entries = await readdir(directory, { withFileTypes: true });
    } catch (error) {
      if (error && typeof error === "object" && error.code === "ENOENT") return;
      throw error;
    }
    entries.sort((left, right) => left.name.localeCompare(right.name, "en"));
    for (const entry of entries) {
      const target = path.join(directory, entry.name);
      if (entry.isDirectory()) await visit(target);
      else if (entry.isFile() && (await stat(target)).isFile()) output.push(target);
    }
  }
  await visit(root);
  return output;
}
